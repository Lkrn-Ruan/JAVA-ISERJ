package modelo;

public class Pessoa {
	
	private Integer idPessoa;
	private String nomePessoa;
	private Integer idadePessoa;
	private String profissaoPessoa;
	private Integer telefonePessoa;
	
	
	public Integer getIdPessoa() {
		return idPessoa;
	}
	
	public void setIdPessoa(Integer idPessoa) {
		this.idPessoa = idPessoa;
	}
	
	public String getNomePessoa() {
		return nomePessoa;
	}
	
	public void setNomePessoa(String nomePessoa) {
		this.nomePessoa = nomePessoa;
	}
	
	public Integer getIdadePessoa() {
		return idadePessoa;
	}
	
	public void setIdadePessoa(Integer idadePessoa) {
		this.idadePessoa = idadePessoa;
	}
	
	public String getProfissaoPessoa() {
		return profissaoPessoa;
	}
	
	public void setProfissaoPessoa(String profissaoPessoa) {
		this.profissaoPessoa = profissaoPessoa;
	}
	/**
	 * @return the telefonoPersona
	 */
	public Integer getTelefonePessoa() {
		return telefonePessoa;
	}
	
	public void setTelefonePessoa(Integer telefonePessoa) {
		this.telefonePessoa = telefonePessoa;
	}
	

}
